function sendScript(id){
const buttons=document.querySelectorAll('.script-btn');
let text="";
buttons.forEach(btn=>{if(btn.getAttribute('onclick')===`sendScript(${id})`)text=btn.innerText;});
appendMessage(text,'user');
setTimeout(()=>{
const reply=responses[id]||"Спасибо за обращение! Наш менеджер уже бежит отвечать вам.";
appendMessage(reply,'bot');
},400);
}
function appendMessage(text,sender){
const msg=document.createElement('div');
msg.className=`msg ${sender}`;
msg.innerText=text;
assistantBody.appendChild(msg);
assistantBody.scrollTop=assistantBody.scrollHeight;
}
